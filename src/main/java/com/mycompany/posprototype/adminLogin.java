    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(box1.getText().equals("") || box2.getText().equals("")){
           JOptionPane.showMessageDialog(null, "error");
       }
       else{
           try{
               PreparedStatement ps = con.prepareStatement("SELECT adminid,userName,password FROM admin WHERE userName = ? AND password = ?");
               String userName = "";
               String pass = "";
               ps.setString(1, box1.getText());
               ps.setString(2, box2.getText());
               try(ResultSet dataRead = ps.executeQuery()){
                   if(dataRead!=null){
                       while(dataRead.next()){
                           id = dataRead.getString("adminid");
                           userName = dataRead.getString("userName");
                           pass = dataRead.getString("password");
                       }
                   }
                   //JOptionPane.showMessageDialog(null, id);
                   if(userName != "" && pass!=""){
                        adminMenu ad1 = new adminMenu(id);
                        this.hide();
                        ad1.show();
                   }
                   else{
                         JOptionPane.showMessageDialog(null, "Invalid username or password");
                   }
               }
           }
           catch(SQLException ee){
               JOptionPane.showMessageDialog(null, ee.toString());
           }
           catch(Exception ll){
               JOptionPane.showMessageDialog(null, ll.toString());
           }
           
       }
    }//GEN-LAST:event_jButton1ActionPerformed

